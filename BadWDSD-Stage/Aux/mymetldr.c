struct ElfHeader32_s
{
    uint8_t e_ident[16];  /* ELF identification */
    uint16_t e_type;      /* object file type */
    uint16_t e_machine;   /* machine type */
    uint32_t e_version;   /* object file version */
    uint32_t e_entry;     /* entry point address */
    uint32_t e_phoff;     /* program header offset */
    uint32_t e_shoff;     /* section header offset */
    uint32_t e_flags;     /* processor-specific flags */
    uint16_t e_ehsize;    /* ELF header size */
    uint16_t e_phentsize; /* size of program header entry */
    uint16_t e_phnum;     /* number of program header entries */
    uint16_t e_shentsize; /* size of section header entry */
    uint16_t e_shnum;     /* number of section header entries */
    uint16_t e_shstrndx;  /* section name string table index */
} __attribute__((packed));

struct ElfPhdr32_s
{
    uint32_t p_type;   /* Segment type */
    uint32_t p_offset; /* Segment file offset */
    uint32_t p_vaddr;  /* Segment virtual address */
    uint32_t p_paddr;  /* Segment physical address */
    uint32_t p_filesz; /* Segment size in file */
    uint32_t p_memsz;  /* Segment size in memory */
    uint32_t p_flags;  /* Segment flags */
    uint32_t p_align;  /* Segment alignment */
} __attribute__((packed));

void LoadElfSpu(uint64_t elfFileAddress)
{
    struct ElfHeader32_s elfHdr;
    DMARead(&elfHdr, elfFileAddress, sizeof(elfHdr));

    if (*((uint32_t *)elfHdr.e_ident) != 0x7F454C46)
        stop(0x69);

    uint64_t curPhdrAddress = (elfFileAddress + elfHdr.e_phoff);

    // Type           Offset   VirtAddr   PhysAddr   FileSiz MemSiz  Flg Align
    // LOAD           0x000080 0x00000800 0x00000800 0x00000 0x0e810 RW  0x80
    // LOAD           0x000100 0x00012c00 0x00012c00 0x24650 0x24650 R E 0x80
    // LOAD           0x024750 0x000372d0 0x000372d0 0x019d0 0x019d0 RW  0x80
  
    for (uint16_t i = 0; i < elfHdr.e_phnum; ++i)
    {
        struct ElfPhdr32_s* phdr = (struct ElfPhdr32_s*)(0x39A00 + (uint32_t)(curPhdrAddress & 0xf));
        DMARead(phdr, curPhdrAddress, sizeof(struct ElfPhdr32_s));

        {
            memset((void*)phdr->p_vaddr, 0, phdr->p_memsz);

            if (phdr->p_filesz > 0)
                DMARead((void*)phdr->p_vaddr, (elfFileAddress + phdr->p_offset), phdr->p_filesz);
        }

        curPhdrAddress += elfHdr.e_phentsize;
    }
}

